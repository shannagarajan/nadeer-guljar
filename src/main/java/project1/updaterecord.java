    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        String path = "jdbc:mysql://localhost:3306/";
        String place = "project1";
        try {
            Class.forName("com.mysql.jdbc.Driver");
            Connection myconnection = DriverManager.getConnection(path + place, "root", "");
            try {
                String q = "select * from newemployee where username=?";
                PreparedStatement mystatement = myconnection.prepareStatement(q);
                mystatement.setString(1, eb.getText());


                ResultSet myresult = mystatement.executeQuery();

                if (myresult.next()) {
                    nb.setText(myresult.getString("name"));
                    ab.setText(myresult.getString("address"));
                    cb.setText(myresult.getString("contact_no"));
                    sb.setText(myresult.getString("salary"));
                    if (myresult.getString("gender").equals("Male")) {
                        mb.setSelected(true);
                    } else if (myresult.getString("gender").equals("Female")) {
                        fb.setSelected(true);
                    }

                    String dob = myresult.getString("dob");
                    String a[] = dob.split("-");
                    jComboBox1.setSelectedItem(a[2]);
                    jComboBox2.setSelectedItem(a[1]);
                    jComboBox3.setSelectedItem(a[0]);


                } else {
                    JOptionPane.showMessageDialog(rootPane, "incorrect memberid");
                }


                mystatement.close();
                myconnection.close();
            } catch (Exception e) {
                //System.out.println("Error in query"+e.getMessage());
                JOptionPane.showMessageDialog(rootPane, "Error in query" + e.getMessage());
            }
        } catch (Exception e) {
            //System.out.println("Error on connnection"+e.getMessage());
            JOptionPane.showMessageDialog(rootPane, "Error in query" + e.getMessage());
        }

    }//GEN-LAST:event_jButton1ActionPerformed

