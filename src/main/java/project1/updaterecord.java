    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        jScrollPane3.setVisible(true);
        eb.setText(null);
        sb.setText(null);
        ab.setText(null);
        cb.setText(null);
        buttonGroup1.clearSelection();
        jComboBox3.setSelectedIndex(0);
        jComboBox2.setSelectedIndex(0);
        jComboBox1.setSelectedIndex(0);
        String path = "jdbc:mysql://localhost:3306/";
        String place = "project1";
        try {
            Class.forName("com.mysql.jdbc.Driver");
            Connection myconnection = DriverManager.getConnection(path + place, "root", "");

            String id, name, add, phone;
            try {
                String q = "select * from newemployee where name like '%" + nb.getText() + "%'";
                PreparedStatement mystatement = myconnection.prepareStatement(q);

                DefaultTableModel mymodel = (DefaultTableModel) jTable1.getModel();

                ResultSet myresult = mystatement.executeQuery();
                mymodel.setRowCount(0);
                if (myresult.next()) {
                    //jTable1.setVisible(true);
                    do {
                        id = myresult.getString("username");
                        name = myresult.getString("name");
                        add = myresult.getString("address");
                        phone = myresult.getString("contact_no");

                        mymodel.addRow(new Object[]{id, name, add, phone});
                    } while (myresult.next());
                } else {
                    if (!nb.getText().isEmpty()) {
                        JOptionPane.showMessageDialog(rootPane, "no records present");
                    }
                }


                mystatement.close();
                myconnection.close();
            } catch (Exception e) {
                //System.out.println("Error in query"+e.getMessage());
                JOptionPane.showMessageDialog(rootPane, "Error in query" + e.getMessage());
            }
        } catch (Exception e) {

            JOptionPane.showMessageDialog(rootPane, "Error in query" + e.getMessage());
        }

    }//GEN-LAST:event_jButton3ActionPerformed

